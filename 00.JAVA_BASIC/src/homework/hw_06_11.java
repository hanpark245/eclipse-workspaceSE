package homework;
public class hw_06_11 
{
	public static void main(String[] args)
	{
		//윤년여부//////////////////////////////////////////////////////////////////////////////////////////////////////////////
		System.out.println("\n--------------윤년계산기---------------");
		
		//-----------------선언------------------------
		int year = 2200;																			//변수 연도
		boolean isLeapYear = ((year % 400) == 0) || (((year % 4) == 0) && ((year % 100) != 0));		//윤년인가 아닌가 논리변수
		
		//-----------------메쏘드-----------------------
		if(isLeapYear)
		{
			System.out.printf("%d년은 윤년입니다\n", year);
		}
		else
		{
			System.out.printf("%d년은 윤년이 아닙니다\n", year);
		}
		//---------------------------------------------------------------------------------------------------------------------
		
		
		
		//문자판단-----------------------------------------------------------///////////////////////////////////////////////////
		System.out.println("\n--------------문자판단-----------------");
		
		//-----------------------선언----------------------------------------
		char x = '0';	//문자
		
		//-----------------메쏘드---------------------------------------------
		if(((int)x >= 44032 && (int)x <= 55203) || ((int)x >= 12593 && (int)x <= 12684))
		{
			System.out.println("문자 " + x + "는(은) 한글입니다.");
		}
		else if((int)x >= 48 && (int)x <= 57)
		{
			System.out.println("문자 " + x + "는(은) 숫자입니다.");
		}
		else if((int)x >= 97 && (int)x <= 122)
		{
			System.out.println("문자 " + x + "는(은) 소문자 알파벳입니다.");
		}
		else if((int)x >= 65 && (int)x <= 90)
		{
			System.out.println("문자 " + x + "는(은) 대문자 알파벳입니다.");
		}
		//---------------------------------------------------------------------------------------------------------------------
		
		
		//아이디의 유효성 판단------------------------------------------------------///////////////////////////////////////////
		System.out.println("\n---------아이디의 유효성 판단----------");
		
		//-----------------선언-----------------------------------------------------
		char id = 'a';		//아이디 이름
		
		//-----------------메쏘드---------------------------------------------------
		if(((int)id >= 97 && (int)id <= 122) || ((int)id >= 65 && (int)id <= 90))
		{
			System.out.println(id + "는(은) 사용가능한 아이디입니다");
		}
		else
		{
			System.out.println(id + "는(은) 사용불가능한 아이디입니다");
		}
		//---------------------------------------------------------------------------------------------------------------------
		
		
		/////////////문자간의 비교---------------------------------------------------------------------------------------------
		boolean testR;
		testR = ('A' < 'B');
		System.out.println("\n-----------문자간의 비교------------");
		System.out.println("A < B = " + testR);
		//---------------------------------------------------------------------------------------------------------------------
		
		/*------------int -> char-----------------------------
		char test3;
		test3 = (char)x;
		System.out.println("(char)x = ", test3);
		*/
	}
}
